/******************************************************************/
/*                    延时函数                                    */
/******************************************************************/
void delayl(uint i)//短延时函数
{
 	while(i--);
}

void delay(uint z) //长延时函数
{
	uint x,y;
	for(x=z;x>0;x--)
		for(y=110;y>0;y--);
}